def main():
    if len(sys.argv) < 2:
        print("Usage: python extract_asset_ids.py <place.rbxlx> [output.lua]")
        sys.exit(1)
    
    input_file = sys.argv[1]
    output_file = sys.argv[2] if len(sys.argv) > 2 else "src/shared/AssetRegistry.lua"
    
    if not os.path.exists(input_file):
        print(f"Error: File not found: {input_file}")
        sys.exit(1)
    
    print(f"Extracting assets from: {input_file}")
    
    # Extract assets
    asset_ids = extract_assets_from_rbxlx(input_file)
    print(f"Found {len(asset_ids)} unique asset IDs")
    
    if not asset_ids:
        print("Warning: No asset IDs found in file")
        sys.exit(0)
    
    # Categorize
    with open(input_file, 'r', encoding='utf-8') as f:
        content = f.read()
    categories = categorize_assets(asset_ids, content)
    
    # Print summary
    print("\nAsset breakdown:")
    for category, ids in categories.items():
        if ids:
            print(f"  {category}: {len(ids)} assets")
    
    # Generate Lua code
    lua_code = generate_lua_registry(asset_ids, categories)
    
    # Write output
    output_path = Path(output_file)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_path, 'w', encoding='utf-8') as f:
        f.write(lua_code)
    
    print(f"\nGenerated AssetRegistry.lua at: {output_path}")
    print("\nNext steps:")
    print("1. Review the generated AssetRegistry.lua")
    print("2. Rename generic asset IDs to meaningful names")
    print("3. Move assets into proper category subfolders")
    print("4. Test in Roblox Studio")

if __name__ == "__main__":
    main()