
#!/usr/bin/env python3
"""
Extract asset IDs from Roblox place files (.rbxlx) and format for AssetRegistry.lua
Usage: python extract_asset_ids.py place.rbxlx [output.lua]
"""

import re
import sys
import os
from pathlib import Path
from typing import Dict, List, Tuple
from collections import defaultdict

def extract_assets_from_rbxlx(file_path: str) -> List[str]:
    """Extract all unique asset IDs from a .rbxlx file."""
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # Find all rbxassetid:// patterns
    pattern = r'rbxassetid://(\d+)'
    matches = re.findall(pattern, content)
    
    # Return unique IDs while preserving order
    seen = set()
    unique = []
    for asset_id in matches:
        if asset_id not in seen:
            seen.add(asset_id)
            unique.append(asset_id)
    
    return unique

def categorize_assets(asset_ids: List[str], content: str) -> Dict[str, List[str]]:
    """Attempt to categorize assets based on context in the XML."""
    categories = {
        'Meshes': [],
        'Textures': [],
        'Audio': [],
        'Particles': [],
        'Animations': []
    }
    
    for asset_id in asset_ids:
        # Find context around the asset ID
        pattern = rf'.{{0,100}}rbxassetid://{re.escape(asset_id)}.{{0,100}}'
        matches = re.findall(pattern, content, re.DOTALL)
        
        if matches:
            context = matches[0].lower()
            
            # Categorize based on context clues
            if any(keyword in context for keyword in ['mesh', 'meshpart', 'specialmesh', 'blockmesh']):
                categories['Meshes'].append(asset_id)
            elif any(keyword in context for keyword in ['texture', 'decall', 'surfacegui', 'image']):
                categories['Textures'].append(asset_id)
            elif any(keyword in context for keyword in ['sound', 'audio', 'music']):
                categories['Audio'].append(asset_id)
            elif any(keyword in context for keyword in ['particle', 'vfx', 'emitter']):
                categories['Particles'].append(asset_id)
            elif any(keyword in context for keyword in ['animation', 'anim']):
                categories['Animations'].append(asset_id)
            else:
                # Default to Meshes if unsure
                categories['Meshes'].append(asset_id)
        else:
            categories['Meshes'].append(asset_id)
    
    return categories

